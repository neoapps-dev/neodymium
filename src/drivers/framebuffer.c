int fb_init(struct multiboot_info *mbd) {
    unsigned int fb_phys = 0;
    unsigned int fb_size;
    if (mbd->flags & (1 << 12)) {
        fb_width = mbd->framebuffer_width;
        fb_height = mbd->framebuffer_height;
        fb_pitch = mbd->framebuffer_pitch;
        fb_bpp = mbd->framebuffer_bpp;
        fb_phys = (unsigned int)(mbd->framebuffer_addr & 0xFFFFFFFFULL);
        fb_size = fb_pitch * fb_height;
        map_fb(fb_phys, fb_size);
        fb_enabled = 1;
        printf("[fb] %dx%d %dbpp pitch=%d phys=0x%x (grub)\n", fb_width, fb_height, fb_bpp, fb_pitch, fb_phys);
        return 0;
    }

    if (vbe_init(1280, 720, 32, &fb_phys) == 0) {
        fb_width = 1280;
        fb_height = 720;
        fb_bpp = 32;
        fb_pitch = fb_width * (fb_bpp / 8);
        fb_size = fb_pitch * fb_height;
        map_fb(fb_phys, fb_size);
        fb_enabled = 1;
        return 0;
    }

    if (vbe_init(1024, 768, 32, &fb_phys) == 0) {
        fb_width = 1024;
        fb_height = 768;
        fb_bpp = 32;
        fb_pitch = fb_width * (fb_bpp / 8);
        fb_size = fb_pitch * fb_height;
        map_fb(fb_phys, fb_size);
        fb_enabled = 1;
        return 0;
    }

    return -1;
}

void fb_putpixel(int x, int y, unsigned int color) {
    if (!fb_enabled || x < 0 || (unsigned int)x >= fb_width || y < 0 || (unsigned int)y >= fb_height) return;
    unsigned int offset = y * fb_pitch + x * (fb_bpp / 8);
    *(unsigned int *)(fb_ptr + offset) = color;
}

void fb_fillrect(int x, int y, int w, int h, unsigned int color) {
    if (!fb_enabled) return;
    unsigned int bpp = fb_bpp / 8;
    for (int row = y; row < y + h && row < (int)fb_height; row++) {
        for (int col = x; col < x + w && col < (int)fb_width; col++) {
            unsigned int offset = row * fb_pitch + col * bpp;
            *(unsigned int *)(fb_ptr + offset) = color;
        }
    }
}

void fb_drawchar(int x, int y, char c, unsigned int fg, unsigned int bg) {
    if (!fb_enabled) return;
    if (c < 0x20 || c > 0x7E) return;
    const unsigned char *glyph = fb_font[c - 0x20];
    unsigned int bpp = fb_bpp / 8;
    for (int row = 0; row < 8; row++) {
        unsigned char line = glyph[row];
        for (int col = 0; col < 8; col++) {
            unsigned int color = (line & (1 << (7 - col))) ? fg : bg;
            int px = x + col;
            int py = y + row;
            if (px >= 0 && (unsigned int)px < fb_width &&
                py >= 0 && (unsigned int)py < fb_height) {
                unsigned int offset = py * fb_pitch + px * bpp;
                *(unsigned int *)(fb_ptr + offset) = color;
            }
        }
    }
}

void fb_drawstring(int x, int y, const char *s, unsigned int fg, unsigned int bg) {
    if (!fb_enabled) return;
    int cx = x;
    while (*s) {
        fb_drawchar(cx, y, *s, fg, bg);
        cx += 10;
        s++;
    }
}

void fb_clear(unsigned int color) {
    if (!fb_enabled) return;
    unsigned int bpp = fb_bpp / 8;
    for (unsigned int y = 0; y < fb_height; y++) {
        for (unsigned int x = 0; x < fb_width; x++) {
            unsigned int offset = y * fb_pitch + x * bpp;
            *(unsigned int *)(fb_ptr + offset) = color;
        }
    }
}

unsigned int fb_rgb(unsigned char r, unsigned char g, unsigned char b) {
    return (0xFF << 24) | (r << 16) | (g << 8) | b;
}

unsigned int fb_get_width(void) { return fb_width; }
unsigned int fb_get_height(void) { return fb_height; }
unsigned int fb_get_bpp(void) { return fb_bpp; }
int fb_is_enabled(void) { return fb_enabled; }
