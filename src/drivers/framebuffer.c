#define VBE_INDEX_PORT 0x01CE
#define VBE_DATA_PORT 0x01CF
#define VBE_ID 0
#define VBE_XRES 1
#define VBE_YRES 2
#define VBE_BPP 3
#define VBE_ENABLE 4
#define VBE_DISABLED 0x00
#define VBE_ENABLED 0x01
#define VBE_LFB_ENABLED 0x40
static unsigned char *fb_ptr;
static unsigned int fb_width;
static unsigned int fb_height;
static unsigned int fb_pitch;
static unsigned int fb_bpp;
static int fb_enabled;
static unsigned short vbe_read(unsigned short idx)
{
    outw(VBE_INDEX_PORT, idx);
    return inw(VBE_DATA_PORT);
}

static void vbe_write(unsigned short idx, unsigned short val)
{
    outw(VBE_INDEX_PORT, idx);
    outw(VBE_DATA_PORT, val);
}

static unsigned int pci_read_bar0(void) {
    for (unsigned int bus = 0; bus < 256; bus++) {
        for (unsigned int dev = 0; dev < 32; dev++) {
            for (unsigned int func = 0; func < 8; func++) {
                unsigned int addr = 0x80000000 | (bus << 16) | (dev << 11) | (func << 8);
                outl(0xCF8, addr);
                unsigned int id = inl(0xCFC);
                if ((id & 0xFFFF) == 0xFFFF) break;
                if ((id & 0xFFFF) == 0x1234 && ((id >> 16) & 0xFFFF) == 0x1111) {
                    outl(0xCF8, addr | 0x10);
                    unsigned int bar0 = inl(0xCFC);
                    return bar0 & 0xFFFFF000;
                }
            }
        }
    }
    return 0xE0000000;
}

static int vbe_init(unsigned int width, unsigned int height, unsigned int bpp, unsigned int *out_phys) {
    if (vbe_read(VBE_ID) < 0xB0C0)return -1;
    vbe_write(VBE_ENABLE, VBE_DISABLED);
    vbe_write(VBE_XRES, width);
    vbe_write(VBE_YRES, height);
    vbe_write(VBE_BPP, bpp);
    vbe_write(VBE_ENABLE, VBE_ENABLED | VBE_LFB_ENABLED);
    if (vbe_read(VBE_XRES) != width || vbe_read(VBE_YRES) != height) return -1;
    *out_phys = pci_read_bar0();
    printf("[fb] vbe init %dx%d %dbpp phys=0x%x\n", width, height, bpp, *out_phys);
    return 0;
}

static void map_fb(unsigned int phys, unsigned int size) {
    unsigned int pages = (size + 0xFFF) / 0x1000;
    for (unsigned int i = 0; i < pages; i++) vmm_map_page((void *)(phys + i * 0x1000), (void *)(phys + i * 0x1000), VMM_WRITABLE);
    fb_ptr = (unsigned char *)(unsigned long)phys;
}

